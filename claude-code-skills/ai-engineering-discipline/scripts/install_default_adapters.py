#!/usr/bin/env python3
"""Plan or install the default Spec / Loop / Verify / Memory adapter stack."""

from __future__ import annotations

import argparse
import json
import shutil
import subprocess
import sys
from dataclasses import dataclass
from pathlib import Path


@dataclass
class InstallOutcome:
    status: str
    detail: str = ""


def default_stack_path() -> Path:
    script = Path(__file__).resolve()
    candidates = [
        script.parents[1] / "adapters" / "default-stack.json",
        script.parents[1] / "references" / "default-stack.json",
    ]
    for candidate in candidates:
        if candidate.is_file():
            return candidate
    raise SystemExit(
        "Could not find default-stack.json. Expected adapters/default-stack.json "
        "from the framework repo or references/default-stack.json from an installed skill."
    )


def run_check_command(command: list[str]) -> bool:
    if not shutil.which(command[0]):
        return False
    return subprocess.run(command, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL).returncode == 0


def run_check_import(module: str) -> bool:
    return (
        subprocess.run(
            [sys.executable, "-c", f"import {module}"],
            stdout=subprocess.DEVNULL,
            stderr=subprocess.DEVNULL,
        ).returncode
        == 0
    )


def status_for(layer: dict[str, object]) -> str:
    install = layer.get("install", {})
    if not isinstance(install, dict):
        return "unknown"
    check = install.get("check")
    if isinstance(check, list) and all(isinstance(x, str) for x in check):
        return "installed" if run_check_command(check) else "missing"
    module = install.get("check_python_import")
    if isinstance(module, str):
        return "installed" if run_check_import(module) else "missing"
    return "unknown"


def command_for(layer: dict[str, object]) -> list[str]:
    install = layer.get("install", {})
    if not isinstance(install, dict):
        return []
    command = install.get("preferred", [])
    if not isinstance(command, list) or not all(isinstance(x, str) for x in command):
        return []
    return command


def executable_command_for(layer: dict[str, object]) -> list[str]:
    command = command_for(layer)
    if command and command[0] == "python":
        command = [sys.executable, *command[1:]]
    # venv 内 `pip install --user` 必然失败(user site 在 venv 不可见);venv 本身已是隔离
    # 目标,直接装进当前环境即可,所以剥掉 --user。venv 外仍保留 --user(免 sudo 全局装)。
    in_venv = sys.prefix != getattr(sys, "base_prefix", sys.prefix)
    if in_venv and "pip" in command and "install" in command:
        command = [arg for arg in command if arg != "--user"]
    return command


def require_layers_dict(stack: dict[str, object]) -> dict[str, dict[str, object]]:
    layers = stack.get("layers")
    if not isinstance(layers, dict):
        raise SystemExit("Invalid stack: missing layers object")
    validated: dict[str, dict[str, object]] = {}
    for layer_name, layer in layers.items():
        if not isinstance(layer, dict):
            raise SystemExit(f"Invalid stack: layer {layer_name!r} must be an object.")
        validated[layer_name] = layer
    return validated


def write_report(
    target: Path,
    layers: dict[str, dict[str, object]],
    statuses: dict[str, str],
    install_notes: dict[str, InstallOutcome] | None = None,
) -> None:
    adapters_dir = target / "docs" / "adapters"
    adapters_dir.mkdir(parents=True, exist_ok=True)
    report = adapters_dir / "default-stack.md"
    lines = [
        "# Default AI Engineering Adapter Stack",
        "",
        "This report is generated by `scripts/install_default_adapters.py`.",
        "",
        "| Layer | Framework | Status | Repository | Why |",
        "|---|---|---|---|---|",
    ]
    for layer_name, layer in layers.items():
        lines.append(
            f"| {layer_name} | {layer['framework']} | {statuses[layer_name]} | "
            f"{layer['repo']} | {layer['why']} |"
        )
    lines.extend(
        [
            "",
            "## Install Commands",
            "",
        ]
    )
    for layer_name, layer in layers.items():
        command = executable_command_for(layer)
        lines.append(f"### {layer_name}: {layer['framework']}")
        lines.append("")
        lines.append("```bash")
        lines.append(" ".join(command) if command else "# no command configured")
        lines.append("```")
        lines.append("")
    if install_notes:
        lines.extend([
            "## Install Results",
            "",
            "| Layer | Outcome | Detail |",
            "|---|---|---|",
        ])
        for layer_name, outcome in install_notes.items():
            detail = outcome.detail.replace("|", "\\|").replace("\n", " ").strip() or "n/a"
            lines.append(f"| {layer_name} | {outcome.status} | {detail} |")
        lines.append("")
    report.write_text("\n".join(lines), encoding="utf-8")
    print(f"wrote: {report}")


def execute_install(layer_name: str, layer: dict[str, object]) -> InstallOutcome:
    command = executable_command_for(layer)
    if not command:
        print(f"skip {layer_name}: no install command")
        return InstallOutcome("skipped", "no install command configured")
    print(f"install {layer_name}: {' '.join(command)}")
    try:
        subprocess.run(command, check=True)
    except FileNotFoundError as exc:
        executable = command[0] if command else "<empty>"
        return InstallOutcome("failed", f"missing executable: {executable} ({exc})")
    except subprocess.CalledProcessError as exc:
        return InstallOutcome("failed", f"command exited with status {exc.returncode}")
    return InstallOutcome("installed", "install command completed")


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("target", nargs="?", default=".", help="Target project path.")
    parser.add_argument("--execute", action="store_true", help="Actually run install commands.")
    parser.add_argument(
        "--layers",
        default="verify",
        help="Comma-separated adapter layers to process. codebase-memory is required separately for impact analysis; Semgrep is the optional security adapter.",
    )
    args = parser.parse_args()

    target = Path(args.target).expanduser().resolve()
    if not target.exists() or not target.is_dir():
        raise SystemExit(f"Target project does not exist: {target}")

    stack = json.loads(default_stack_path().read_text(encoding="utf-8"))
    layers = require_layers_dict(stack)

    selected = [name.strip() for name in args.layers.split(",") if name.strip()]
    unknown = [name for name in selected if name not in layers]
    if unknown:
        raise SystemExit(f"Unknown layers: {', '.join(unknown)}")

    statuses: dict[str, str] = {}
    install_notes: dict[str, InstallOutcome] = {}
    for layer_name, layer in layers.items():
        if layer_name not in selected:
            # 必需层(如 impact 知识图谱)即使本次没选,也别显示成 "not selected" 误导用户
            # 以为没启用——它是硬闸,只是不由本命令安装(需另行编译,见 scripts/code_graph.py)。
            if layer.get("required"):
                statuses[layer_name] = "REQUIRED (build separately — see scripts/code_graph.py; run doctor to check)"
            else:
                statuses[layer_name] = "not selected"
            continue
        if not layer.get("runtime_integrated", False):
            if layer.get("required"):
                # 硬依赖但非 pip 安装(知识图谱 MCP,需编译):框架通过 scripts/code_graph.py 调 CLI
                statuses[layer_name] = "REQUIRED (build separately — see scripts/code_graph.py)"
            elif layer.get("agent_integrated", False):
                statuses[layer_name] = "optional MCP (used by the agent, not the framework)"
            else:
                # 风格参考层(Spec/Loop/Memory):框架不调用它们,无需安装
                statuses[layer_name] = "reference-only (not used by the framework)"
            continue
        statuses[layer_name] = status_for(layer)

    if args.execute:
        for layer_name in selected:
            layer = layers[layer_name]
            if not layer.get("runtime_integrated", False):
                continue  # 风格参考层不安装
            if statuses[layer_name] == "installed":
                print(f"skip {layer_name}: already installed")
                install_notes[layer_name] = InstallOutcome("skipped", "already installed")
            else:
                outcome = execute_install(layer_name, layer)
                install_notes[layer_name] = outcome
                statuses[layer_name] = status_for(layer)
                if outcome.status == "failed" and statuses[layer_name] != "installed":
                    print(f"failed {layer_name}: {outcome.detail}")
                elif outcome.status == "installed" and statuses[layer_name] != "installed":
                    # 安装命令成功,但 console script 不在当前 shell PATH(常见于 pip --user / uv tool)
                    print(
                        f"note {layer_name}: install command succeeded but check still reports "
                        f"'{statuses[layer_name]}'; the executable may need a new shell or PATH refresh."
                    )
    else:
        print("Dry run only. Re-run with --execute to install missing adapters.")

    write_report(target, layers, statuses, install_notes if args.execute else None)

    print()
    print("| Layer | Framework | Status |")
    print("|---|---|---|")
    for layer_name, layer in layers.items():
        print(f"| {layer_name} | {layer['framework']} | {statuses[layer_name]} |")

    if any(note.status == "failed" for note in install_notes.values()):
        return 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
